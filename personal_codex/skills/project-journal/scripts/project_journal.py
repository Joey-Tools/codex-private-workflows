#!/usr/bin/env python3
"""Manage cross-repo project journal indexes for Joey's Codex workflows."""

from __future__ import annotations

import argparse
import dataclasses
import datetime as dt
import json
import os
import pathlib
import re
import shlex
import stat
import subprocess
import sys
from typing import Any


VALID_STATUSES = ("active", "blocked", "completed", "superseded", "abandoned")
REQUIRED_FIELDS = (
    "id",
    "title",
    "status",
    "created",
    "updated",
    "branch",
    "pr",
    "supersedes",
    "superseded_by",
)
DEFAULT_INDEX = pathlib.PurePosixPath("docs/project_journal/INDEX.md")
JOURNAL_ROOT = pathlib.PurePosixPath("docs/project_journal")
HOOK_NAMES = ("post-merge", "post-checkout", "post-rewrite")
HOOK_BEGIN = "# BEGIN PROJECT-JOURNAL-INDEX"
HOOK_END = "# END PROJECT-JOURNAL-INDEX"
INDEX_GENERATED_LINE = (
    "Generated by `project_journal.py`. Do not edit this local index by hand."
)
DATE_RE = re.compile(r"^\d{4}-\d{2}-\d{2}$")


class UserError(Exception):
    """A clean user-facing error."""


@dataclasses.dataclass(frozen=True)
class JournalEntry:
    path: pathlib.Path
    rel_path: str
    fields: dict[str, Any]

    @property
    def entry_id(self) -> str:
        return str(self.fields.get("id", "")).strip()

    @property
    def title(self) -> str:
        return str(self.fields.get("title", "")).strip()

    @property
    def status(self) -> str:
        return str(self.fields.get("status", "")).strip()

    @property
    def created(self) -> str:
        return str(self.fields.get("created", "")).strip()

    @property
    def updated(self) -> str:
        return str(self.fields.get("updated", "")).strip()


def _run_git(repo: pathlib.Path, *args: str) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        ["git", "-C", str(repo), *args],
        check=False,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
    )


def _resolve_repo(repo_arg: str) -> pathlib.Path:
    candidate = pathlib.Path(repo_arg).expanduser().resolve()
    result = _run_git(candidate, "rev-parse", "--show-toplevel")
    if result.returncode != 0:
        detail = (
            result.stderr.strip() or result.stdout.strip() or "not a git repository"
        )
        raise UserError(f"{candidate} is not a git repository: {detail}")
    return pathlib.Path(result.stdout.strip()).resolve()


def _git_path(repo: pathlib.Path, rel: str) -> pathlib.Path:
    result = _run_git(repo, "rev-parse", "--git-path", rel)
    if result.returncode != 0:
        detail = (
            result.stderr.strip() or result.stdout.strip() or "git path lookup failed"
        )
        raise UserError(f"failed to resolve git path {rel!r}: {detail}")
    path = pathlib.Path(result.stdout.strip())
    if not path.is_absolute():
        path = repo / path
    return path


def _to_repo_rel(repo: pathlib.Path, path_arg: str) -> str:
    path = pathlib.Path(path_arg)
    if path.is_absolute():
        resolved = path.resolve()
    else:
        resolved = (repo / path).resolve()
    try:
        rel = resolved.relative_to(repo)
    except ValueError as exc:
        raise UserError(
            f"output path must resolve inside the repository: {resolved}"
        ) from exc
    return rel.as_posix()


def _strip_quotes(value: str) -> str:
    value = value.strip()
    if len(value) >= 2 and value[0] == value[-1] and value[0] in {"'", '"'}:
        return value[1:-1]
    return value


def _parse_scalar(value: str) -> Any:
    value = value.strip()
    if value == "[]":
        return []
    if value.startswith("[") and value.endswith("]"):
        inner = value[1:-1].strip()
        if not inner:
            return []
        return [
            _strip_quotes(part.strip()) for part in inner.split(",") if part.strip()
        ]
    return _strip_quotes(value)


def _parse_frontmatter(path: pathlib.Path) -> dict[str, Any]:
    lines = path.read_text(encoding="utf-8").splitlines()
    if not lines or lines[0].strip() != "---":
        raise UserError(f"{path}: missing frontmatter")

    end = None
    for index, line in enumerate(lines[1:], start=1):
        if line.strip() == "---":
            end = index
            break
    if end is None:
        raise UserError(f"{path}: unterminated frontmatter")

    fields: dict[str, Any] = {}
    current_key: str | None = None
    for raw_line in lines[1:end]:
        if not raw_line.strip():
            continue
        if raw_line.startswith((" ", "\t")):
            if current_key is None:
                continue
            stripped = raw_line.strip()
            if stripped.startswith("- "):
                existing = fields.get(current_key)
                if not isinstance(existing, list):
                    existing = []
                    fields[current_key] = existing
                existing.append(_strip_quotes(stripped[2:].strip()))
            continue
        if ":" not in raw_line:
            raise UserError(f"{path}: invalid frontmatter line: {raw_line}")
        key, raw_value = raw_line.split(":", 1)
        key = key.strip()
        if not key:
            raise UserError(f"{path}: empty frontmatter key")
        fields[key] = _parse_scalar(raw_value)
        current_key = key
    return fields


def _as_list(value: Any) -> list[str]:
    if value is None or value == "":
        return []
    if isinstance(value, list):
        return [str(item).strip() for item in value if str(item).strip()]
    return [str(value).strip()]


def _journal_paths(repo: pathlib.Path) -> list[pathlib.Path]:
    root = repo / JOURNAL_ROOT
    if not root.exists():
        return []
    paths: list[pathlib.Path] = []
    for path in root.rglob("*.md"):
        if _is_generated_index(path):
            continue
        paths.append(path)
    return sorted(paths)


def _is_generated_index(path: pathlib.Path) -> bool:
    try:
        lines = path.read_text(encoding="utf-8", errors="replace").splitlines()
    except OSError:
        return False
    return (
        len(lines) >= 3
        and lines[0] == "# Project Journal Index"
        and lines[2] == INDEX_GENERATED_LINE
    )


def _load_entries(repo: pathlib.Path) -> tuple[list[JournalEntry], list[str]]:
    entries: list[JournalEntry] = []
    issues: list[str] = []
    for path in _journal_paths(repo):
        rel_path = path.relative_to(repo).as_posix()
        try:
            fields = _parse_frontmatter(path)
        except UserError as exc:
            issues.append(str(exc))
            continue
        entries.append(JournalEntry(path=path, rel_path=rel_path, fields=fields))
    issues.extend(_validate_entries(entries))
    return entries, issues


def _validate_date(value: str) -> bool:
    if not DATE_RE.match(value):
        return False
    try:
        dt.date.fromisoformat(value)
    except ValueError:
        return False
    return True


def _validate_entries(entries: list[JournalEntry]) -> list[str]:
    issues: list[str] = []
    ids: dict[str, JournalEntry] = {}
    rel_paths = {entry.rel_path for entry in entries}

    for entry in entries:
        label = entry.rel_path
        for field in REQUIRED_FIELDS:
            if field not in entry.fields:
                issues.append(f"{label}: missing required field {field!r}")

        if not entry.entry_id:
            issues.append(f"{label}: field 'id' must not be empty")
        elif entry.entry_id in ids:
            issues.append(f"{label}: duplicate id {entry.entry_id!r}")
        else:
            ids[entry.entry_id] = entry

        if not entry.title:
            issues.append(f"{label}: field 'title' must not be empty")
        if entry.status not in VALID_STATUSES:
            issues.append(
                f"{label}: invalid status {entry.status!r}; expected one of {', '.join(VALID_STATUSES)}"
            )
        if not entry.created:
            issues.append(f"{label}: field 'created' must not be empty")
        elif not _validate_date(entry.created):
            issues.append(f"{label}: invalid created date {entry.created!r}")
        if not entry.updated:
            issues.append(f"{label}: field 'updated' must not be empty")
        elif not _validate_date(entry.updated):
            issues.append(f"{label}: invalid updated date {entry.updated!r}")

    valid_targets = set(ids) | rel_paths
    for entry in entries:
        for superseded in _as_list(entry.fields.get("supersedes")):
            if superseded not in valid_targets:
                issues.append(
                    f"{entry.rel_path}: supersedes target {superseded!r} does not match a journal id or path"
                )
        target = str(entry.fields.get("superseded_by", "")).strip()
        if target and target not in valid_targets:
            issues.append(
                f"{entry.rel_path}: superseded_by target {target!r} does not match a journal id or path"
            )
        if entry.status == "superseded":
            if not target:
                issues.append(
                    f"{entry.rel_path}: superseded entries must set 'superseded_by'"
                )

    return issues


def _markdown_escape(value: str) -> str:
    return value.replace("|", "\\|").replace("\n", " ").strip()


def _link_from_output(output_path: pathlib.Path, entry: JournalEntry) -> str:
    rel = os.path.relpath(entry.path, start=output_path.parent)
    return pathlib.PurePosixPath(rel).as_posix()


def _entry_sort_key(entry: JournalEntry) -> tuple[str, str, str]:
    return (entry.updated, entry.created, entry.title.lower())


def _render_index(
    repo: pathlib.Path, output_path: pathlib.Path, entries: list[JournalEntry]
) -> str:
    grouped = {status: [] for status in VALID_STATUSES}
    for entry in entries:
        if entry.status in grouped:
            grouped[entry.status].append(entry)
    for values in grouped.values():
        values.sort(key=_entry_sort_key, reverse=True)

    lines = [
        "# Project Journal Index",
        "",
        INDEX_GENERATED_LINE,
        "",
        "## Summary",
        "",
    ]
    for status in VALID_STATUSES:
        lines.append(f"- `{status}`: {len(grouped[status])}")
    lines.append("")

    if not entries:
        lines.extend(["No project journal entries found.", ""])
        return "\n".join(lines)

    for status in VALID_STATUSES:
        lines.extend([f"## {status.title()}", ""])
        if not grouped[status]:
            lines.extend(["_None._", ""])
            continue
        lines.append("| Updated | Title | ID | Branch | PR |")
        lines.append("| --- | --- | --- | --- | --- |")
        for entry in grouped[status]:
            href = _link_from_output(output_path, entry)
            title = _markdown_escape(entry.title)
            branch = _markdown_escape(str(entry.fields.get("branch", "")).strip())
            pr = _markdown_escape(str(entry.fields.get("pr", "")).strip())
            lines.append(
                f"| {entry.updated} | [{title}]({href}) | `{_markdown_escape(entry.entry_id)}` | {branch} | {pr} |"
            )
        lines.append("")
    return "\n".join(lines)


def _ensure_exclude(repo: pathlib.Path, output_rel: str) -> pathlib.Path:
    exclude_path = _git_path(repo, "info/exclude")
    exclude_path.parent.mkdir(parents=True, exist_ok=True)
    existing = exclude_path.read_text(encoding="utf-8") if exclude_path.exists() else ""
    lines = existing.splitlines()
    if output_rel not in lines:
        if existing and not existing.endswith("\n"):
            existing += "\n"
        existing += f"{output_rel}\n"
        exclude_path.write_text(existing, encoding="utf-8")
    return exclude_path


def command_generate(args: argparse.Namespace) -> int:
    repo = _resolve_repo(args.repo)
    output_rel = _to_repo_rel(repo, args.output)
    output_path = repo / output_rel
    entries, issues = _load_entries(repo)
    if issues:
        raise UserError(
            "journal validation failed before index generation:\n" + "\n".join(issues)
        )

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(_render_index(repo, output_path, entries), encoding="utf-8")
    if args.ensure_exclude:
        _ensure_exclude(repo, output_rel)
    print(output_path)
    return 0


def command_validate(args: argparse.Namespace) -> int:
    repo = _resolve_repo(args.repo)
    _entries, issues = _load_entries(repo)
    if issues:
        for issue in issues:
            print(issue, file=sys.stderr)
        return 1
    print("Project journal validation passed.")
    return 0


def _hook_body() -> str:
    python_exe = shlex.quote(sys.executable)
    script_path = shlex.quote(str(pathlib.Path(__file__).resolve()))
    return f"""#!/bin/sh
{HOOK_BEGIN}
hook_name=${{0##*/}}
if [ "$hook_name" = "post-rewrite" ] && [ ! -t 0 ]; then
  cat >/dev/null || true
fi

if [ "${{PROJECT_JOURNAL_INDEX_DISABLE:-}}" = "1" ]; then
  exit 0
fi

repo=$(git rev-parse --show-toplevel 2>/dev/null)
if [ -z "$repo" ]; then
  exit 0
fi

tmp_log=$(mktemp "${{TMPDIR:-/tmp}}/project-journal-index.XXXXXX" 2>/dev/null)
if [ -z "$tmp_log" ]; then
  tmp_log="${{TMPDIR:-/tmp}}/project-journal-index.$$"
  : > "$tmp_log" 2>/dev/null || tmp_log=""
fi

if [ -n "$tmp_log" ]; then
  if ! {{
    date -u '+%Y-%m-%dT%H:%M:%SZ'
    {python_exe} {script_path} generate --repo "$repo" --output {DEFAULT_INDEX.as_posix()} --ensure-exclude
  }} > "$tmp_log" 2>&1; then
    log_path=$(git rev-parse --git-path project-journal-index.log 2>/dev/null)
    if [ -z "$log_path" ]; then
      log_path="$repo/.git/project-journal-index.log"
    fi
    cat "$tmp_log" >> "$log_path" 2>/dev/null || true
  fi
  rm -f "$tmp_log"
else
  {python_exe} {script_path} generate --repo "$repo" --output {DEFAULT_INDEX.as_posix()} --ensure-exclude >/dev/null 2>&1 || true
fi

exit 0
{HOOK_END}
"""


def _hooks_dir_from_config(repo: pathlib.Path, raw_path: str) -> pathlib.Path:
    if not raw_path.strip():
        raise UserError(
            "core.hooksPath is empty; unset it or set a non-empty hook directory before installing"
        )
    if raw_path == os.devnull:
        raise UserError(
            "core.hooksPath points at os.devnull; cannot install project journal hooks"
        )
    hooks_dir = pathlib.Path(raw_path).expanduser()
    if not hooks_dir.is_absolute():
        hooks_dir = repo / hooks_dir
    hooks_dir = hooks_dir.resolve()
    allowed_roots = [repo.resolve(), _git_path(repo, ".").resolve()]
    common_dir = _run_git(repo, "rev-parse", "--git-common-dir")
    if common_dir.returncode == 0 and common_dir.stdout.strip():
        common_path = pathlib.Path(common_dir.stdout.strip())
        if not common_path.is_absolute():
            common_path = repo / common_path
        allowed_roots.append(common_path.resolve())
    if not any(_is_relative_to(hooks_dir, root) for root in allowed_roots):
        raise UserError(
            "core.hooksPath resolves outside the repository or git directory; "
            "set a repo-local hook directory before installing"
        )
    return hooks_dir


def _default_hooks_dir(repo: pathlib.Path) -> pathlib.Path:
    hooks_dir = _git_path(repo, "hooks")
    if hooks_dir.is_symlink():
        raise UserError(
            f"{hooks_dir} is a symlink; refusing to install hooks through hook directory links"
        )
    return hooks_dir.resolve()


def _hooks_dir(repo: pathlib.Path) -> pathlib.Path:
    for scope in ("--worktree", "--local"):
        configured = _run_git(repo, "config", scope, "--get", "core.hooksPath")
        if configured.returncode == 0:
            return _hooks_dir_from_config(repo, configured.stdout.strip())
    effective = _run_git(repo, "config", "--get", "core.hooksPath")
    if effective.returncode == 0:
        if not effective.stdout.strip():
            raise UserError(
                "core.hooksPath is empty; unset it or set a non-empty hook directory before installing"
            )
        raise UserError(
            "effective core.hooksPath comes from non-local git config; "
            "set a repo-local core.hooksPath or unset the global/system value before installing"
        )
    return _default_hooks_dir(repo)


def _hook_path(repo: pathlib.Path, name: str) -> pathlib.Path:
    return _hooks_dir(repo) / name


def _preflight_hook_targets(repo: pathlib.Path) -> list[pathlib.Path]:
    hook_paths = [_hook_path(repo, name) for name in HOOK_NAMES]
    for hook_path in hook_paths:
        if hook_path.is_symlink():
            raise UserError(
                f"{hook_path} is a symlink; refusing to overwrite hook links"
            )
        if not hook_path.exists():
            continue
        existing = hook_path.read_text(encoding="utf-8", errors="replace")
        if HOOK_BEGIN not in existing or HOOK_END not in existing:
            raise UserError(
                f"{hook_path} already exists and is not managed by project_journal.py"
            )
        before, _marker, rest = existing.partition(HOOK_BEGIN)
        _managed, _end_marker, after = rest.partition(HOOK_END)
        before_lines = [line for line in before.splitlines() if line.strip()]
        if before_lines not in ([], ["#!/bin/sh"]) or after.strip():
            raise UserError(
                f"{hook_path} contains unmanaged content outside the project journal marker block"
            )
    return hook_paths


def _install_hook(hook_path: pathlib.Path) -> pathlib.Path:
    hook_path.parent.mkdir(parents=True, exist_ok=True)
    if hook_path.is_symlink():
        raise UserError(f"{hook_path} is a symlink; refusing to overwrite hook links")
    if hook_path.exists():
        existing = hook_path.read_text(encoding="utf-8", errors="replace")
        if HOOK_BEGIN not in existing or HOOK_END not in existing:
            raise UserError(
                f"{hook_path} already exists and is not managed by project_journal.py"
            )
    hook_path.write_text(_hook_body(), encoding="utf-8")
    mode = hook_path.stat().st_mode
    hook_path.chmod(mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)
    return hook_path


def command_install_hooks(args: argparse.Namespace) -> int:
    repo = _resolve_repo(args.repo)
    hook_paths = _preflight_hook_targets(repo)
    _ensure_exclude(repo, DEFAULT_INDEX.as_posix())
    installed = [_install_hook(path) for path in hook_paths]
    for path in installed:
        print(path)
    return 0


def _path_date(path: pathlib.Path) -> dt.date | None:
    parts = path.parts
    for index, part in enumerate(parts):
        if part == "sessions" and index + 3 < len(parts):
            try:
                return dt.date(
                    int(parts[index + 1]),
                    int(parts[index + 2]),
                    int(parts[index + 3]),
                )
            except ValueError:
                continue
    return None


def _rollout_last_seen(path: pathlib.Path) -> str:
    dated = _path_date(path)
    if dated is not None:
        return dated.isoformat()
    mtime = dt.datetime.fromtimestamp(path.stat().st_mtime, tz=dt.timezone.utc)
    return mtime.date().isoformat()


def _rollout_in_window(path: pathlib.Path, cutoff: dt.date) -> bool:
    dated = _path_date(path)
    if dated is not None:
        return dated >= cutoff
    mtime = dt.datetime.fromtimestamp(path.stat().st_mtime, tz=dt.timezone.utc)
    return mtime.date() >= cutoff


def _walk_for_cwds(value: Any) -> list[str]:
    found: list[str] = []
    if isinstance(value, dict):
        for key, item in value.items():
            if key in {
                "cwd",
                "workdir",
                "working_dir",
                "working_directory",
            } and isinstance(item, str):
                found.append(item)
            else:
                found.extend(_walk_for_cwds(item))
    elif isinstance(value, list):
        for item in value:
            found.extend(_walk_for_cwds(item))
    return found


def _extract_cwds(rollout: pathlib.Path) -> list[str]:
    cwds: list[str] = []
    with rollout.open("r", encoding="utf-8", errors="replace") as handle:
        for line in handle:
            line = line.strip()
            if not line:
                continue
            try:
                payload = json.loads(line)
            except json.JSONDecodeError:
                continue
            cwds.extend(_walk_for_cwds(payload))
    return cwds


def _known_source_root_from_temp_path(path: pathlib.Path) -> pathlib.Path | None:
    parts = path.parts
    for index, part in enumerate(parts):
        if part != ".codex-tmp" or index + 2 >= len(parts):
            continue
        temp_name = parts[index + 1]
        workspace_name = parts[index + 2]
        if temp_name.startswith("isolated-review-") and workspace_name == "workspace":
            source = pathlib.Path(*parts[:index])
            return source if source != pathlib.Path() else pathlib.Path("/")
    return None


def _is_relative_to(path: pathlib.Path, parent: pathlib.Path) -> bool:
    try:
        path.resolve().relative_to(parent.resolve())
    except ValueError:
        return False
    return True


def _git_output_path(repo: pathlib.Path, raw_path: str) -> pathlib.Path:
    path = pathlib.Path(raw_path.strip())
    if not path.is_absolute():
        path = repo / path
    return path.resolve()


def _source_root_from_linked_worktree(repo: pathlib.Path) -> pathlib.Path | None:
    git_dir = _run_git(repo, "rev-parse", "--git-dir")
    common_dir = _run_git(repo, "rev-parse", "--git-common-dir")
    if git_dir.returncode != 0 or common_dir.returncode != 0:
        return None

    git_dir_path = _git_output_path(repo, git_dir.stdout)
    common_dir_path = _git_output_path(repo, common_dir.stdout)
    if git_dir_path == common_dir_path:
        return None

    source = (
        common_dir_path.parent if common_dir_path.name == ".git" else common_dir_path
    )
    source_root = _repo_root_for_existing_path(source)
    if source_root is None or source_root == repo:
        return None
    return source_root


def _repo_root_for_existing_path(path: pathlib.Path) -> pathlib.Path | None:
    while not path.exists():
        parent = path.parent
        if parent == path:
            return None
        path = parent
    result = _run_git(path, "rev-parse", "--show-toplevel")
    if result.returncode != 0:
        return None
    return pathlib.Path(result.stdout.strip()).resolve()


def _repo_root_for_path(
    path_text: str, *, codex_home: pathlib.Path | None = None
) -> pathlib.Path | None:
    path = pathlib.Path(path_text).expanduser()
    if not path.is_absolute():
        return None
    source = _known_source_root_from_temp_path(path)
    if source is not None:
        source_root = _repo_root_for_existing_path(source)
        if source_root is not None:
            return source_root
    root = _repo_root_for_existing_path(path)
    if root is None:
        return None
    if codex_home is not None and _is_relative_to(root, codex_home / "worktrees"):
        source_root = _source_root_from_linked_worktree(root)
        if source_root is not None:
            return source_root
    return root


def _unique_preserving_order(values: list[str]) -> list[str]:
    seen: set[str] = set()
    unique: list[str] = []
    for value in values:
        if value in seen:
            continue
        seen.add(value)
        unique.append(value)
    return unique


def _has_hook_marker(repo: pathlib.Path) -> bool:
    for name in HOOK_NAMES:
        try:
            hook = _hook_path(repo, name)
        except UserError:
            return False
        if not hook.exists():
            return False
        try:
            content = hook.read_text(encoding="utf-8", errors="replace")
        except OSError:
            return False
        if HOOK_BEGIN not in content or HOOK_END not in content:
            return False
    return True


def _is_excluded(repo: pathlib.Path, rel: str) -> bool:
    exclude = _git_path(repo, "info/exclude")
    if not exclude.exists():
        return False
    return rel in exclude.read_text(encoding="utf-8", errors="replace").splitlines()


def _discover_repos(codex_home: pathlib.Path, since_days: int) -> list[dict[str, Any]]:
    sessions = codex_home / "sessions"
    if not sessions.exists():
        return []
    cutoff = (dt.datetime.now(dt.timezone.utc) - dt.timedelta(days=since_days)).date()
    seen: dict[pathlib.Path, dict[str, Any]] = {}
    resolved_roots: dict[str, pathlib.Path | None] = {}
    script = pathlib.Path(__file__).resolve()

    for rollout in sorted(sessions.rglob("rollout-*.jsonl")):
        if not rollout.is_file() or not _rollout_in_window(rollout, cutoff):
            continue
        last_seen = _rollout_last_seen(rollout)
        roots_for_rollout: set[pathlib.Path] = set()
        for cwd in _unique_preserving_order(_extract_cwds(rollout)):
            if cwd not in resolved_roots:
                resolved_roots[cwd] = _repo_root_for_path(cwd, codex_home=codex_home)
            root = resolved_roots[cwd]
            if root is None:
                continue
            roots_for_rollout.add(root)
        for root in roots_for_rollout:
            row = seen.setdefault(
                root,
                {
                    "repo": str(root),
                    "last_seen": last_seen,
                    "rollout_count": 0,
                },
            )
            row["last_seen"] = max(str(row["last_seen"]), last_seen)
            row["rollout_count"] = int(row["rollout_count"]) + 1

    results: list[dict[str, Any]] = []
    for root, row in sorted(seen.items(), key=lambda item: item[0].as_posix()):
        journal_root = root / JOURNAL_ROOT
        index_rel = DEFAULT_INDEX.as_posix()
        row.update(
            {
                "has_journal_dir": journal_root.is_dir(),
                "journal_count": len(_journal_paths(root)),
                "has_index": (root / index_rel).is_file(),
                "index_ignored": _is_excluded(root, index_rel),
                "hooks_installed": _has_hook_marker(root),
                "install_command": f"{shlex.quote(sys.executable)} {shlex.quote(str(script))} install-hooks --repo {shlex.quote(str(root))}",
                "generate_command": f"{shlex.quote(sys.executable)} {shlex.quote(str(script))} generate --repo {shlex.quote(str(root))} --output {index_rel} --ensure-exclude",
            }
        )
        results.append(row)
    return results


def command_discover_repos(args: argparse.Namespace) -> int:
    codex_home = pathlib.Path(args.codex_home).expanduser().resolve()
    rows = _discover_repos(codex_home, args.since_days)
    if args.json_output:
        print(json.dumps(rows, ensure_ascii=False, indent=2, sort_keys=True))
        return 0
    for row in rows:
        print(
            f"{row['repo']}\tlast_seen={row['last_seen']}\t"
            f"journals={row['journal_count']}\thooks={row['hooks_installed']}"
        )
    return 0


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    subparsers = parser.add_subparsers(dest="command", required=True)

    generate = subparsers.add_parser(
        "generate", help="Generate a local project journal index."
    )
    generate.add_argument("--repo", required=True)
    generate.add_argument("--output", default=DEFAULT_INDEX.as_posix())
    generate.add_argument("--ensure-exclude", action="store_true")
    generate.set_defaults(func=command_generate)

    validate = subparsers.add_parser(
        "validate", help="Validate project journal frontmatter."
    )
    validate.add_argument("--repo", required=True)
    validate.set_defaults(func=command_validate)

    hooks = subparsers.add_parser(
        "install-hooks", help="Install local Git hooks for index refresh."
    )
    hooks.add_argument("--repo", required=True)
    hooks.set_defaults(func=command_install_hooks)

    discover = subparsers.add_parser(
        "discover-repos", help="Discover Codex-touched Git repos."
    )
    discover.add_argument(
        "--codex-home",
        default=os.environ.get("CODEX_HOME", "~/.codex"),
        help="Codex home to inspect. Defaults to CODEX_HOME or ~/.codex.",
    )
    discover.add_argument("--since-days", type=int, default=30)
    discover.add_argument("--json", action="store_true", dest="json_output")
    discover.set_defaults(func=command_discover_repos)

    return parser


def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    try:
        return int(args.func(args))
    except UserError as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
